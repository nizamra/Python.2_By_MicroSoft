long_word = "characteristics"
print(long_word[3]) # the fourth letter
print(long_word[0]) # the first letter
print(long_word[-1]) # prints the last letter
print(long_word[1:3]) # addressing the  2nd & 3rd characters
print(long_word[:3]) # from the start to the fourth letter
print(long_word[3:]) # from the fourth letter to the end
print(long_word[:]) # the full word
print(long_word[::2]) #step by two
print(long_word[1:9:2]) # from 1 to 9 by 2 steps a time
print(long_word[::-1]) # flip the world
